		<h1>POLÍTICA DE PRIVACIDAD</h1>

		<i>Última actualización : 08/08/23</i>

		<p>En esta Política de Privacidad, se describen nuestras políticas y procedimientos sobre la recopilación, el uso y la divulgación de su información cuando utiliza Great Pet, también referido como <b>Servicio</b> y <b>Sitio Web</b>. Al hacer uso de nuestro servicio, usted acepta la recopilación y el uso de su información de acuerdo con esta Política de Privacidad.</p>

		<h2>Normativa Legal</h2>

		Nuestras políticas de privacidad están sujetas a la ley 25.326, que regula la protección de los datos personales en Argentina.

		<h2>Recopilación y uso de los datos</h2>

		<h3>Tipos de datos que recopilamos</h3>
			
		<h4>Datos que proporcionan los usuarios</h4>
			
		Cuando crea una cuenta de Great Pet, nos proporciona su información personal que incluye su nombre y apellido, contraseña, y dirección de correo electrónico. También puede optar por agregar un número de teléfono y/o celular.
			
		<h4>Datos que se generan durante el uso de nuestros servicios</h4>
			
		<p>Ciertos datos se recopilan automáticamente cuando se utiliza nuestro servicio, que incluye, entre otros:</p>

		<li>Dirección IP.</li>
		<li>Sistema Operativo.</li>
		<li>Tipo de Navegador.</li>
		<li>Versión del Navegador.</li>
		<li>Las páginas de nuestro servicio que usted visita.</li>
		<li>La fecha y hora de su visita.</li>

		<h3>Uso de sus datos personales</h3>

		Great Pet utiliza la información que nos proporciona al crear una cuenta (nombre, apellido, contraseña y dirección de correo electrónico) exclusivamente con el propósito de brindarle acceso a nuestros servicios de manera segura y personalizada. No utilizaremos sus datos personales para mejorar nuestros servicios ni para contactarlo con fines promocionales o publicitarios.

		<h2>Conservación y eliminación de la información</h2>

		Mantenemos los datos recopilados mientras el usuario tenga una cuenta activa en nuestro sitio web. Si el usuario decide cerrar su cuenta, su información personal se eliminará automáticamente y retendremos ciertos datos durante un periodo prolongado de tiempo para cumplir con obligaciones legales y garantizar la integridad de nuestros servicios.
		<p>Los usuarios pueden solicitar la eliminación de sus cuentas en cualquier momento a través de su perfil.</p>

		<h2>Intercambio y divulgación de la información</h2>

		No venderemos, alquilaremos ni divulgaremos su información personal a terceros sin su consentimiento, excepto cuando sea necesario para cumplir con las leyes aplicables o proteger nuestros derechos legales.

		<h2>Seguridad de la información</h2>

		La seguridad de sus datos personales es importante para nosotros, y hemos tomado medidas para proteger su información personal contra acceso no autorizado, divulgación o alteración. Sin embargo, ningún método de transmisión por Internet o método de almacenamiento electrónico es completamente seguro. Por lo tanto, no podemos garantizar la seguridad absoluta de su información debido a las posibles vulnerabilidades de Internet ni a la ausencia total de hackers u otros que accedan de modo fraudulento a los datos personales.

		<h2>Cambios en la política de privacidad</h2>

		Es posible que actualicemos esta política en el futuro. Los cambios a esta Política de privacidad entrarán en vigor una vez publicados en esta página, y la fecha de última actualización al comienzo de esta Política de Privacidad se modificará en consecuencia.
		<p>Si hace uso de nuestros servicios tras una actualización, consideraremos que acepta la política actualizada.</p>

